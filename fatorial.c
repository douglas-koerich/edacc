long long int fatorial(int n) {
    //             (M) ----------------------------> 1
    long long int f = 1;
    // f = 1 x 2 x 3 x ... x i x ... x (n-1) x n
    //   (M) --------------------------------------> 1
    int i = 1;
    //       (R) ----------------------------------> 1 x (n+1)
    while (i <= n) {
    //   (M) (A) ----------------------------------> 2n
        f = f * i; // f *= i;
    //   (M) (A) ----------------------------------> 2n
        i = i + 1; // ++i; ou f *= i++;
    }
    // (M) ----------------------------------------> 1
    return f; // ----------------------------------> =============
} // ----------------------------------------------> T(n) = 5n + 4
  // ----------------------------------------------> Complexidade: O(n)